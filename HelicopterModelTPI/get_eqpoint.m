function [xr, ur]=get_eqpoint(x1r)
% punkt rownowagi
K = 7.4055e+03;
a = 0.270635379992858;
c = 4.921249191542748;
al = 0.495440761917189;
d = 0.125788155178429;

p = [];
p(1) = 1.8199;
p(2) = 0.2683;
p(3) = 1.7818;
p(4) = -0.7 - c/d*sin(x1r+al);
x3r = roots(p);
x3r = x3r(imag(x3r) == 0);
xr=[x1r;0;x3r];
ur = (0.012571932380354)*x3r^3 + (-0.001137539289392)*x3r^2 + (0.160285847209929)*x3r -0.005559905060608;
end